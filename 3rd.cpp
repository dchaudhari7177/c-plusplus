#include<iostream>
using namespace std;
int main(){
int a=5, b=5;
cout<<"following are arithmatic operators in c++"<<endl;

cout<<"the value of a+b is:"<<a+b<<endl;
cout<<"the value of a-b is:"<<a-b<<endl;
cout<<"the value of a*b is:"<<a*b<<endl;
cout<<"the value of a/b is:"<<a/b<<endl;

cout<<"the value of a++ is:"<<a++<<endl;
cout<<"the value of a-- is:"<<a--<<endl;
cout<<"the value of b++ is:"<<b++<<endl;
cout<<"the value of b-- is:"<<b--<<endl;

cout<<"following are the comparison operators in C++";

cout<<"the value of a==b is:"<<(a==b)<<endl;
cout<<"the value of a!=b is:"<<(a!=b)<<endl;
cout<<"the value of a>b is:"<<(a>b)<<endl;
cout<<"the value of a<b is:"<<(a<b)<<endl;
cout<<"the value of a<=b is:"<<(a<=b)<<endl;
cout<<"the value of a>=b is:"<<(a>=b)<<endl;

cout<<"following are the logical operators in c++"<<endl;

cout<<"the value of (a==b) && (a>b) this logical operator is:"<<((a==b) && (a>b))<<endl;
cout<<"the value of (a==b) || (a>b) this logical operator is:"<<((a==b) || (a>b))<<endl;
cout<<"the value of (!(a==b)) this logical operator is:"<<(!(a==b))<<endl;

return 0;
}